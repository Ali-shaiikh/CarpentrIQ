"""PDF generator — WeasyPrint A4 quote PDF per CLAUDE.md § Quote PDF Spec.

Hallmark policy:
  - By default every PDF carries a "Generated by CarpentrIQ" footer hallmark.
  - Pass show_hallmark=False to omit it (requires a paid pdf_credit on the
    carpenter account; enforced by the caller, not here).

Image strategy:
  - All images (carpenter photo, business logo) are downloaded and base64-encoded
    before calling WeasyPrint so rendering never makes outbound HTTP requests.
  - QR code generated in-process via qrcode library.
  - Google Fonts loaded via CSS @import; falls back to Georgia/Arial if unreachable.
"""

from __future__ import annotations

import asyncio
import base64
import io
import logging
from datetime import datetime, timezone
from html import escape

import httpx
import qrcode

from app.config import settings

logger = logging.getLogger(__name__)

