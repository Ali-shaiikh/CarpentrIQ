def _build_header_center(name: str, phone: str, city: str, logo_b64: str) -> str:
    logo_html = (
        f'<img class="carpenter-logo" src="data:image/png;base64,{logo_b64}" alt="logo"><br>'
        if logo_b64 else ""
    )
    return (
        f'{logo_html}'
        f'<div class="carpenter-name">{name}</div>'
        f'<div class="carpenter-sub">+91 {phone} · {city}</div>'
    )


def _build_line_items_html(line_items: list[dict]) -> str:
    rows = []
    for i, item in enumerate(line_items, 1):
        display = escape(item.get("display_name") or item.get("item_type") or "")
        dims = escape(item.get("dimensions_display") or "—")
        spec = _summarise_spec(item.get("material_breakdown") or [])
        price = float(item.get("final_price") or 0)
        rows.append(
            f'<tr>'
            f'<td>{i}</td>'
            f'<td><div class="item-name">{display}</div>'
            f'<div class="item-spec">{spec}</div></td>'
            f'<td style="font-size:8.5pt">{dims}</td>'
            f'<td style="font-size:8.5pt">{spec}</td>'
            f'<td class="price-col">₹{price:,.0f}</td>'
            f'</tr>'
        )
    return "\n".join(rows) if rows else '<tr><td colspan="5">No items</td></tr>'


def _build_summary_html(
    subtotal: float, tax: float, total: float, advance: float, balance: float
) -> str:
    return f"""
<div class="summary-wrap">
  <table class="summary">
    <tr><td>Subtotal</td><td class="amount">₹{subtotal:,.0f}</td></tr>
    {"" if not tax else f'<tr><td>Tax</td><td class="amount">₹{tax:,.0f}</td></tr>'}
    <tr class="divider total-row">
      <td>Total</td><td class="amount">₹{total:,.0f}</td>
    </tr>
    <tr class="advance-row">
      <td>Advance (35%)</td><td class="amount">₹{advance:,.0f}</td>
    </tr>
    <tr class="divider">
      <td>Balance on Completion</td><td class="amount">₹{balance:,.0f}</td>
    </tr>
  </table>
</div>"""


def _build_hallmark(qr_b64: str) -> str:
    return (
        '<div class="hallmark">'
        'Generated by <span>CarpentrIQ</span><br>'
        '<span style="font-size:7pt;color:#888">carpentriq.in</span>'
        '</div>'
    )


def _summarise_spec(breakdown: list[dict]) -> str:
    key_materials = {
        "plywood_bwp_8x4": "BWP Ply",
        "mdf_18mm_8x4": "18mm MDF",
        "laminate_sqft": "Laminate",
        "hinge_softclose": "Soft-close hinges",
        "drawer_slide_bb": "BB drawer slides",
        "hydraulic_lift_mechanism": "Hydraulic lift",
    }
    labels = []
    for item in breakdown:
        name = item.get("name", "")
        if name in key_materials:
            labels.append(key_materials[name])
    return ", ".join(labels[:3]) or "—"


def _format_valid_until(created_raw: str | None, validity_days: int) -> str:
    try:
        from datetime import timedelta
        dt = datetime.fromisoformat(created_raw) if created_raw else datetime.now(timezone.utc)
        return (dt + timedelta(days=validity_days)).strftime("%d %b %Y")
    except Exception:
        return "—"


# ── Image helpers ─────────────────────────────────────────────────────────────

async def _download_b64(url: str | None) -> str:
    """Download image from URL and return base64 string. Returns '' on failure."""
    if not url:
        return ""
    try:
        async with httpx.AsyncClient(timeout=10) as client:
            resp = await client.get(url)
            resp.raise_for_status()
            return base64.b64encode(resp.content).decode()
    except Exception as exc:
        logger.warning("Image download failed for PDF (%s): %s", url, exc)
        return ""


def _generate_qr_b64(url: str) -> str:
    try:
        buf = io.BytesIO()
        qrcode.make(url).save(buf, format="PNG")
        return base64.b64encode(buf.getvalue()).decode()
    except Exception as exc:
        logger.warning("QR generation failed: %s", exc)
        return ""
