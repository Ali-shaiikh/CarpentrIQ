SLATE = "#4A5568"

PDF_CREDIT_PRICE_INR = 99


class PDFGenerator:

    async def generate_quote_pdf(
        self,
        quote: dict,
        carpenter: dict,
        show_hallmark: bool = True,
    ) -> bytes:
        """Render quote as A4 PDF bytes.

        quote      — output of _quote_to_dict() from quote.py
        carpenter  — dict with keys: name, phone, city, photo_url,
                     business_logo_url (optional)
        show_hallmark — False suppresses the CarpentrIQ footer badge
                        (requires a paid credit consumed by the caller)
        """
        photo_b64 = await _download_b64(carpenter.get("photo_url"))
        logo_b64 = await _download_b64(carpenter.get("business_logo_url"))
        share_token = quote.get("share_token", "")
        qr_url = f"{settings.frontend_url}/quote/{share_token}" if share_token else ""
        qr_b64 = _generate_qr_b64(qr_url) if qr_url else ""

        html_src = _build_html(quote, carpenter, photo_b64, logo_b64, qr_b64, show_hallmark)

        from weasyprint import HTML  # lazy import — needs native pango/gobject libs
        pdf_bytes: bytes = await asyncio.get_event_loop().run_in_executor(
            None,
            lambda: HTML(string=html_src).write_pdf(),
        )
        return pdf_bytes

    def _generate_qr(self, url: str) -> bytes:
        buf = io.BytesIO()
        qrcode.make(url).save(buf, format="PNG")
        return buf.getvalue()


